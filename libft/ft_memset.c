#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*s2;

	i = 0;
	s2 = b;
	while (len--)
	{
		s2[i] = (unsigned char)c;
		i++;
	}
	return (s2);
}
