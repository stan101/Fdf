#include "libft.h"

int		ft_isalnum(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
			|| (c >= 060 && c <= 071))
	{
		return (1);
	}
	return (0);
}
